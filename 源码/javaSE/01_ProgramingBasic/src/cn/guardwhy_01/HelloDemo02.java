package cn.guardwhy_01;

/**
 * System.out.println()：输出后追加换行
 * System.out.print(): 输出后不追加换行
 */
public class HelloDemo02 {
    public static void main(String[] args) {
        // 输出后追加换行
        System.out.println("hello world");
        
        // 输出后不追加换行
        System.out.print("hello world");
        System.out.print("hello world");
    }
}
