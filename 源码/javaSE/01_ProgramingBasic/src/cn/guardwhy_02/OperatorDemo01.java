package cn.guardwhy_02;

/**
 * 算数运算符的运算
 */
public class OperatorDemo01 {
    public static void main(String[] args) {
        // 定义整形变量
        int i = 10;
        int j = 3;
        // 加法操作
        System.out.println(i + "+" + j + "=" + (i+j));
        // 减法操作
        System.out.println(i + "-" + j + "=" + (i-j));
    }
}
